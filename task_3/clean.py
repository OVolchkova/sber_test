# Регулярки
URL_PATTERN = re.compile(r"https?://\S+|t\.me/\S+|www\.\S+")

# Эмодзи - вырезаем, потому что для анализа не нужны
EMOJI_PATTERN = re.compile(
    "["
    "\U0001F600-\U0001F64F"  # смайлики
    "\U0001F300-\U0001F5FF"  # символы
    "\U0001F680-\U0001F6FF"  # транспорт
    "\U0001F1E0-\U0001F1FF"  # флаги
    "\U00002700-\U000027BF"  # символы
    "\U0001F900-\U0001F9FF"  # доп. эмодзи
    "]+",
    flags=re.UNICODE,
)

# Хэштеги вида #SBER
HASHTAG_PATTERN = re.compile(r"[#]([A-ZА-Я]{2,6})\b")

# Для схлопывания пробелов
MULTI_SPACE = re.compile(r"\s+")

###############################################################################
# Удаляет из текста URL, эмодзи, лишние пробелы
def clean_text(text):
    if not isinstance(text, str):
        return ""
    
    text = html.unescape(text)
    text = URL_PATTERN.sub(" ", text)
    text = EMOJI_PATTERN.sub(" ", text)
    text = MULTI_SPACE.sub(" ", text)
    return text.strip()

###############################################################################
# Ищет в тексте тикеры из хэштегов и по словарю
def extract_tickers(text):
    if not isinstance(text, str):
        return []
    
    tickers = set()
    
    # сначала хэштеги
    for match in HASHTAG_PATTERN.findall(text):
        tickers.add(match.upper())
    
    # потом заглавные слова длиной 3-5 букв - сверяем со словарём
    for word in re.findall(r"\b[A-ZА-Я]{3,5}\b", text):
        if word in MOEX_TICKERS:
            tickers.add(word)
    
    return sorted(tickers)

###############################################################################
# Основная функция (чистит текст, извлекает тикеры)
def clean_dataframe(df):
    df = df.copy()
    
    df["text_clean"] = df["text"].apply(clean_text)
    df["tickers"] = df["text"].apply(extract_tickers)
    df["text_len"] = df["text_clean"].str.len()
    
    return df